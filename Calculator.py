import math

class Calculator:
    def __init__(self):
        pass

    def Plus(self, number1, number2):
        number1 = number1 + number2

    def Minus(self, number1, number2):
        number1 = number1 - number2

    def Multiply(self, number1, number2):
        number1 = number1 * number2

    def Divise(self, number1, number2):
        number1 = number1 / number2

    def SquareRoot(self, number):
        number = math.SquareRoot(number)

    def Square(self, number):
        number = math.Square(number)

    def Fraction(self, number):
        number = 1 / number

    def Modulo(self, number1, number2):
        number = number1 % number2
